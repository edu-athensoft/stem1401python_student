"""
istitle()
returns True if the string is a titlecased string.
If not, it returns False.
"""

s1 = 'Python Is Good.'
print(s1.istitle())

s1 = 'Python is good.'
print(s1.istitle())

s = 'This Is @ Symbol.'
print(s.istitle())

s = '99 Is A Number'
print(s.istitle())

s = 'PYTHON'
print(s.istitle())