"""
list - access element, item

access = retrieve or write

index starts from 0
"""


mylist = ['a','b','c','d']

# retrieve or read values
mylist[0]
mylist[1]
mylist[2]
mylist[3]
print(mylist[0])
print(mylist[1])
print(mylist[2])
print(mylist[3])
print(mylist)

# write or update values
mylist[0] = 'a1'
mylist[1] = 'b2'
mylist[2] = 'c3'
mylist[3] = 'd4'
print(mylist)

# negative index
mylist = ['a','b','c','d']
print(mylist[3])
print(mylist[-1])
print(mylist[2])
print(mylist[-2])
print(mylist[1])
print(mylist[-3])
print(mylist[0])
print(mylist[-4])


