# arithmetic
x=5
y = 12

result = x + y
print("x + y = {}".format(result))
print()

result = x - y
print("x - y = {}".format(result))
print()

result = x * y
print("x * y = {}".format(result))
print()

result = x / y
print("x / y = {}".format(result), type(result))
print()

# modulus
result = x % y
print("x % y = {}".format(result))
print()

# floor division
result = 5 // 2
print("x // y = {}".format(result))
print()



