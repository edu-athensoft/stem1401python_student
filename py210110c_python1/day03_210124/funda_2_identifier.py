"""
Identifier(s)

A name given for python entities (var, const, func, class, etc.)

"""


# rule 1
# 0-9a-zA-Z_

a = 10
print(a)

a_ = 12
print(a_)

a1 = 123
print(a1)

_a = 234
print(_a)

# a-b = 123

a_b = 123

# &b = 123

Abc = 345
print(Abc)

ABC1 = 567
print(ABC1)


# rule 2
# starting with a-zA-Z _
# 1a = 123
# 1_ = 123

_123 = 345


# rule 3
# keywords are invalid for identifiers
# for = 1

For = 1
