# data type of set

list1 = [1,2,3,4,5]
print(list1, type(list1))

tuple1 = (1,2,3,4,5)
print(tuple1, type(tuple1))

set1 = {1,2,3,4,5}
print(set1, type(set1))

set2 = {1,1,2,2,3,3}
print(set2)

set3 = {3,2,1}
print(set3)

set4 = {'a',2,5.7}
print(set4)

set5 = {1,2,(1,23)}
print(set5)

# convert list to set
print(set([1,3,2,1,2]))
