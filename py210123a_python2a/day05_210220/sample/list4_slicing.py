# list slicing

my_list = ['p','r','o','g','r','a','m','i','z']

# output: ogr
print(my_list[2:5])

# what if left index > right index
print(my_list[7:5])

# slicing from left - 'prog'
print(my_list[0:4])
print(my_list[:4])
print(my_list[:-5])

# slicing to end - 'miz'
print(my_list[6:9])
print(my_list[6:])
print(my_list[-3:])

# slicing all
print(my_list[:])

# slicing only one
print(my_list[5:6])








