# string method


# split a string into list
# string.split()
my_str= "This will split all words into a list"
print(my_str)
a = my_str.split()
print(a, type(a))
print()

# string.split(symbol)
my_str= "This,will,split,all,words.into.a list"
print(my_str)
a = my_str.split(',')
print(a, type(a))
print()






