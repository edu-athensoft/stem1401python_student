"""
methods of tuple

count(x) - return occurrence times of x in the tuple
index(x) - return the first occurrence of x in the tuple
index(x, start)
index(x, start, end)
"""
tuple1 = (1,1,2,2,2)

# count(x)
num_1 = tuple1.count(1)
num_2 = tuple1.count(2)
print(num_1)
print(num_2)
print()

# index(x)
pos_1 = tuple1.index(1)
pos_2 = tuple1.index(2)
print(pos_1)
print(pos_2)

