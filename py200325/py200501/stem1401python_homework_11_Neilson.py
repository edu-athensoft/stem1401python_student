"""
Printing out a which is 3x4 so 3 rows and 4 columns
"""

a = [
    ["Magic", "Nothing", "Hello", "Crocodile"],
    [1, 2, 3, 4],
    ["Simple", "Math", "French", "Science"]
]
print(a)  # printing the entire matrix
print(a[0])  # printing the first row of the matrix
print(a[0][2])  # printing the element "Hello" from the matrix
print(a[1][1])  # printing the element 2 from the matrix
