# arithmetic operator
#
x = 3
y = 5

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x // y)  # floor division
print(x % y)  # modulus
print(x ** y)  # exponent
print("=====================")

# comparison operator
x = 3
y = 5

print(x > y)
print(x >= y)
print(x < y)
print(x <= y)
print(x == y)
print(x != y)
print("=====================")

# logical operator
x = True
y = False

print(x and y)
print(x or y)
print(not x)
print(not y)
print("=====================")
