"""
tuple - access element
"""

my_tuple1 = (1, 2, 3, 4, 5, 6, 7, 8)

# index
print(my_tuple1[0])
print(my_tuple1[1])
print(my_tuple1[2])

# negative index
print(my_tuple1[-1])
print(my_tuple1[-2])
print(my_tuple1[-3])

my_tuple2 = (1, (11,22), 3, 4, 5, 6, 7, 8)
print(my_tuple2[1][0])
