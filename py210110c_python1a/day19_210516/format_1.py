"""
String formatting

"""

# integer
print("The number is |{:d}|".format(123))

# floating
print("The number is |{:f}|".format(123.456789))

# add width
print("The number is |{:5d}|".format(123))

print("The number is |{:8.3f}|".format(123.456789))


# padding
print("The number is |{:05d}|".format(123))

print("The number is |{:08.3f}|".format(123.456789))


