"""
Number formatting types
"""

# 1. decimal integer
print("The integer is:{:d}".format(32))

# 2. corresponding unicode character
print("The unicode character is:{:c}".format(21))

# 3. binary format
print("The binary number is:{:b}".format(23))

# 4. octal format
print("The octal number is:{:o}".format(24))

# 5. hexadecimal format
print("The hexadecimal number is:{:x}".format(23))

# 6. exponential notation
print("The exponential notation is:{:e}".format(21))

# 7. fixed point number
print("The number is:{:f}".format(23.435345332432))

# 8. general format
print("The number is:{:g}".format(324325.9334567))

# 9. percentage
print("The percentage is:{:%}".format(325))


