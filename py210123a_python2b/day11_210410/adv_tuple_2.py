"""
tuple
indexing

accessing element/item in a tuple
"""


tuple1 = (2,4,6,8,10)

# index
print("tuple1[0] = ",tuple1[0])
print("tuple1[1] = ",tuple1[1])

# negative index
print("tuple1[-1] = ",tuple1[-1])
print("tuple1[-2] = ",tuple1[-2])

# slice
st1 = tuple1[:2]
print(st1)

st2 = tuple1[2:]
print(st2)

st3 = tuple1[1:3]
print(st3)




